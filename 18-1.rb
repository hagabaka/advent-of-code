require 'matrix'

def next_state(state)
  side_length = state.row_count
  Matrix.build(state.row_count) do |row, column|
    (top, left) = [row, column].map {|index| [index - 1, 0].max}
    (bottom, right) = [row, column].map {|index| [index + 1, side_length - 1].min}
    (neightbor_top, neighbor_left) = [row - top, column - left]
    neighbors = state.minor(top..bottom, left..right).each_with_index.
      reject do |value, neighbor_row, neighbor_column|
        [neighbor_row, neighbor_column] == [neightbor_top, neighbor_left]
      end.map {|value, _| value}
    lighted_neighbors = neighbors.count(true)
    if state[row, column]
      lighted_neighbors == 2 || lighted_neighbors == 3
    else
      lighted_neighbors == 3 
    end
  end
end

def display_state(state)
  (0...state.row_count).map do |row|
    state.row(row).map do |value|
      if value
        '#'
      else
        '.'
      end
    end.to_a.join('') + "\n"
  end.to_a.join('')
end

def parse_input(input)
  Matrix[*input.lines.map {|line| line.strip.chars.map {|char| char == '#'}}]
end

input = DATA.read
state = parse_input(input)
display_state(state) == input or abort

100.times do |i|
  new_state = next_state(state)
  print display_state(new_state)
  puts i + 1
  if new_state == state
    break
  end
  state = new_state
end
puts state.count(true);

