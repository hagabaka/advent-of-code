require 'set'
places = Set.new
distances = {}
graph = DATA.read.scan(/^(.+) to (.+) = (\d+)$/).
each do |source, destination, distance|
  places.add source
  places.add destination
  distances[Set[source, destination]] = distance.to_i
end
lengths = places.to_a.permutation.map do |path|
  path.each_cons(2).reduce(0) do |distance, (source, destination)|
    distance + distances[Set[source, destination]]
  end
end
puts lengths.min
